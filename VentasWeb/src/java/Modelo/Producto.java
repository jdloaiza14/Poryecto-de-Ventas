package Modelo;

/**
 *
 * @author od948
 */
public class Producto {
    
    int id;
    String nom, stock, estado;
    double precio;

    public Producto() {
    }

    public Producto(int id, String nom, String stock, String estado, double precio) {
        this.id = id;
        this.nom = nom;
        this.stock = stock;
        this.estado = estado;
        this.precio = precio;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getStock() {
        return stock;
    }

    public void setStock(String stock) {
        this.stock = stock;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }
    
    
}
